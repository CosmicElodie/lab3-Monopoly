public class Square
{
    private String squareName;

    /**
     * Constructor
     * @param newSquareName
     */
    public Square(String newSquareName)
    {
        squareName = newSquareName;
    }

    /**
     * Getter
     * @return name of the current square
     */
    public String getSquareName()
    {
        return squareName;
    }
}
